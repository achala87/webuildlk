                <p>
                    Sri Lanka gains Independance from the British Collonizers on Friday, February 4th 1948.

                    During the 5th Century BC, Indo-Aryan migrants (Prince Wijaya) sailed from near modern day Bengal at Odisha in India. They married into the local
                    veddha (Naga, Raksha, Yaksa) or indeginous communities and the Sinhala race was created, becoming the most powerful clan in the island. 
                    
                    Sri lanka has over 2500 years of continuously chronicled history written in the Mahawamsa. 
                    There is historical evidence to suggest Jaffana area having predominantly tamil occupation since the Arya chakrawarthi kingdom from before the 14th century A.D. 

                    The Portugese arrive in Sri Lanka, the first westerners with intent on colonization in 1505. They settle in a few coastal villages
                    and in Colombo. In 1658 the Dutch force out the Portugese and establish control across the island except at the Central highlands, unable to concquer the Kandyan
                    kingdom. 

                    In 1796 the British take control over the island. In 1815, at last through treachery the Kandyan Kingdom is also concured by the British, gaining them full control over the island. 
                    The British starts bringing Tamil laborers from southern India to work in tea, coffee and coconut plantations and expand the road and rail way networks to export natural resources including Pearls, Gems, Spices, Timber and Ivory.
