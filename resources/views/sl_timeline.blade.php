                    1949. Sinhala-only language law was introced in 1958, 1956 by Solomon W.R.D Bandaranaike elected on wave of 
                    Sinhalese nationalism. This made Sinhala the sole official language and other measures introduced to bolster Sinhalese and Buddhist 
                    feeling were developed. More than 100 Tamils and some Sinhalese were killed in widespread violence after Tamil 
                    parliamentarians protest against these laws. In 1958 Anti-Tamil riots leave over 200 dead and Thousands of Tamils displaced.

                    SWRD Bandaranaike assassinate by a Buddhist Monk in 1959 (Kelaniya Raja maha viharaya chief monk arrested on conspiracy charges as he was unhappy with way Bandaranaiyake treated him).
                    SWRDs widow, Sirimawo Bandaranaike continuous nationalists socialist reforms after coming to power since her husbands demise. Under NP Perera's direction bans on shipment of rice,
                    flour and other essentials create social havoc and UNP (United National Party) wins election in 1965 and attempts to reverse nationalisation measures.
